public class ComparisonOperation {
    public static void main(String[] args) {
        // available operator: <, >, <=, >=, ==, !=
        // comparison will return a boolean value (true/false)

        int a = 100;
        int b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
